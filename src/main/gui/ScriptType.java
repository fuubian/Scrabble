package main.gui;

/**
 * Represents the different types of scripts that the help-Button should show.
 * 
 * @author raydin
 *
 */
public enum ScriptType {

  /**
   * Script of main menu.
   */
  MAIN_MENU,

  /**
   * Script of the host lobby.
   */
  HOST_LOBBY,

  /**
   * Script of the tutorial lobby.
   */
  TUTORIAL_LOBBY,

  /**
   * Script of the ingame session.
   */
  IN_GAME;
}
