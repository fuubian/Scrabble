package main.model;

/**
 * The GameState class represents a game state.
 * 
 * @author sekeller
 */
public enum GameState {

  /**
   * The state before the game play.
   */
  PREPERATION,

  /**
   * The state during the game play.
   */
  PLAY,

  /**
   * The state after the game play.
   */
  GAME_OVER;

}
